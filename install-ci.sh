# APK de Método Solar

Proyecto Android nativo que presenta la aplicación web en una experiencia de pantalla completa e incluye permisos de ubicación, cámara y selección de fotos.

## Compilar

Abre la carpeta `android-apk` con Android Studio y selecciona **Build > Build APK(s)**. El APK de prueba se genera en `app/build/outputs/apk/debug/app-debug.apk`.

También se incluye un flujo de GitHub Actions que genera el APK automáticamente.
